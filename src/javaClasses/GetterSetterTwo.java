package javaClasses;

public class GetterSetterTwo {
	
	private String city;
	private int RollNo;
	
	public String getcity() {
		return city;
		}
	
	public void setcity(String City) {
		city = City;
	}

	public int getRollNo() {
		return RollNo;
		
	}
	public void setRollNo(int rollNo) {
		RollNo = rollNo;
	}
}
