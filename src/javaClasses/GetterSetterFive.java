package javaClasses;

public class GetterSetterFive {
	
	private String Books;
	private String Author;
	
	public String getBooks() {
		return Books;
	}

	public void setBooks(String Books) {
		Books = Books;
	}
	
	public String getAuthor() {
		return Author;
	}
	public void setAuthor(String A) {
		Author = A;
	}
}
